package de.meetingapps.meetingportal.meetComEntitiesGenossenschaftSys;

public class EgxPostTelefon {
    
	public String art;
    public String telefonart;
    public int telefonartnr;
    public String telefonnummer;
    public String bemerkung;
    
    
	public String getArt() {
		return art;
	}
	public void setArt(String art) {
		this.art = art;
	}
	public String getTelefonart() {
		return telefonart;
	}
	public void setTelefonart(String telefonart) {
		this.telefonart = telefonart;
	}
	public int getTelefonartnr() {
		return telefonartnr;
	}
	public void setTelefonartnr(int telefonartnr) {
		this.telefonartnr = telefonartnr;
	}
	public String getTelefonnummer() {
		return telefonnummer;
	}
	public void setTelefonnummer(String telefonnummer) {
		this.telefonnummer = telefonnummer;
	}
	public String getBemerkung() {
		return bemerkung;
	}
	public void setBemerkung(String bemerkung) {
		this.bemerkung = bemerkung;
	}
    
    
}
