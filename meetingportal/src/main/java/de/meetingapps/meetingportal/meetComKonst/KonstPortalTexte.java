    /**pSubTextNr=0 oder 1, kommt aus parameterTewxtLang[5], aber nur bei Beteilungserhöhungen*/
    public static int mailTextNrBasis(int pAuftragsArt, String pSubTextNr) {
        int subTextArt = 0;
        if (pSubTextNr != null) {
            switch (pSubTextNr) {
            case "0":
                subTextArt = 0;
                break;//Überweisung
            case "1":
                subTextArt = 1;
                break;//Sepa
            }
        }
        switch (pAuftragsArt) {
        case KonstAuftragArt.ANBINDUNG_AKTIENREGISTER_ANTRAG_BETEILIGUNGSERHOEHUNG_ZEICHNUNG_OHNE_AUFFUELLBETRAG:
            if (subTextArt == 0) {
                return 2028;
            } else {
                return 2151;
            }
        case KonstAuftragArt.ANBINDUNG_AKTIENREGISTER_ANTRAG_BETEILIGUNGSERHOEHUNG_ZEICHNUNG_UND_AUFFUELLBETRAG:
            if (subTextArt == 0) {
                return 2029;
            } else {
                return 2152;
            }
        case KonstAuftragArt.ANBINDUNG_AKTIENREGISTER_ANTRAG_BETEILIGUNGSERHOEHUNG_OHNE_ZEICHNUNG_MIT_AUFFUELLBETRAG:
            if (subTextArt == 0) {
                return 2030;
            } else {
                return 2153;
            }
        case KonstAuftragArt.ANBINDUNG_AKTIENREGISTER_ANTRAG_BETEILIGUNGSERHOEHUNG_ZEICHNUNG_OHNE_AUFFUELLBETRAG_TEXTFORM:
            if (subTextArt == 0) {
                return 2326;
            } else {
                return 2327;
            }
        }
        return 0;

    }

