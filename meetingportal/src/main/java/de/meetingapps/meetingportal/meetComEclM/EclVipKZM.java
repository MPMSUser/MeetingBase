package de.meetingapps.meetingportal.meetComEclM;

import java.io.Serializable;

import de.meetingapps.meetingportal.meetComEntities.EclVipKZ;
import jakarta.enterprise.context.SessionScoped;
import jakarta.inject.Named;

@SessionScoped
@Named
public class EclVipKZM implements Serializable {
    private static final long serialVersionUID = 13434130248480493L;

    private String kuerzel;
    private String beschreibung;

    public void copyFrom(EclVipKZ pVipKZ) {
        kuerzel = pVipKZ.kuerzel;
        beschreibung = pVipKZ.beschreibung;
    }

    public String getKuerzel() {
        return kuerzel;
    }

    public void setKuerzel(String kuerzel) {
        this.kuerzel = kuerzel;
    }

    public String getBeschreibung() {
        return beschreibung;
    }

    public void setBeschreibung(String beschreibung) {
        this.beschreibung = beschreibung;
    }

}
